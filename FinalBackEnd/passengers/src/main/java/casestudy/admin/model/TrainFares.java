package casestudy.admin.model;

public class TrainFares {
	public int acFare;
	public int sleeperFare;
	
	public TrainFares() {}

	public int getAcFare() {
		return acFare;
	}

	public void setAcFare(int acFare) {
		this.acFare = acFare;
	}

	public int getSleeperFare() {
		return sleeperFare;
	}

	public void setSleeperFare(int sleeperFare) {
		this.sleeperFare = sleeperFare;
	}

}
